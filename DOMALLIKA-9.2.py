# 9.2. Різниця між числами
# Є набір чисел (float або int). Вам потрібно знайти різницю між найбільшим (максимум) і найменшим (мінімум) елементом.
# Ваша функція difference має вміти працювати з невизначеною кількістю аргументів. Якщо аргументів немає, то функція повертає 0 (нуль).
# Якщо з 3-м тестом будуть проблеми, використовуйте функцію округлення round(x, 2), де х це число, яке потрібно округлити.
# Вх. Дані: Змінна кількість аргументів як числа (int, float).
# Вих. Дані: Різниця між максимумом і мінімумом як число (int, float).
#
# Приклади:
#
# assert difference(1, 2, 3) == 2, 'Test1'
# assert difference(5, -5) == 10, 'Test2'
# assert difference(10.2, -2.2, 0, 1.1, 0.5) == 12.4, 'Test3'
# assert difference() == 0, 'Test4'
# print('OK')
# V1
def difference(*args):
    """
    Функция для нахождения разницы между максимальным и минимальным элементом в наборе чисел.
    :param args: Набор чисел (int, float).
    :return: Разница между максимумом и минимумом как число (int, float). Если аргументов нет, возвращает 0.
    """
    if not args:
        return 0

    # Находим максимум и минимум в наборе чисел
    max_value = max(args)
    min_value = min(args)

    # Возвращаем разницу между максимумом и минимумом
    return max_value - min_value

# Тесты
# Тест 1: Разница между 1, 2 и 3 должна быть 2
# Тест 2: Разница между 5 и -5 должна быть 10
# Тест 3: Разница между 10.2, -2.2, 0, 1.1 и 0.5 должна быть 12.4
# Тест 4: Если аргументов нет, результат должен быть 0
assert difference(1, 2, 3) == 2, 'Test1'
assert difference(5, -5) == 10, 'Test2'
assert round(difference(10.2, -2.2, 0, 1.1, 0.5), 2) == 12.4, 'Test3' #  функция difference вызывается, и результат округляется до двух знаков после запятой с помощью функции round()
assert difference() == 0, 'Test4'
print('OK')

