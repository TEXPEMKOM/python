# 7.4. Пошук спільних елементів
# Напишіть функцію common_elements, яка згенерує два списки елементів з генераторного виразу (range) для 100 елементів, за наступними правилом:
# Один список з числами кратними 3, інший з кратними числами 5.
# За допомогою множин створіть набір з числами, які є в обох множинах (перетин).
# Функція повинна повернути цю множину як результат своєї роботи.
#
#
# def common_elements():
#     pass
#
# assert common_elements() == {0, 75, 45, 15, 90, 60, 30}

def common_elements():
    """
    Функция для создания двух списков чисел, кратных 3 и 5, и нахождения их пересечения.
    :return: Множество чисел, которые кратны и 3, и 5.
    """
    # Генерация списка чисел кратных 3
    multiples_of_3 = {x for x in range(100) if x % 3 == 0}

    # Генерация списка чисел кратных 5
    multiples_of_5 = {x for x in range(100) if x % 5 == 0}

    # Нахождение пересечения двух множеств
    common_set = multiples_of_3 & multiples_of_5

    return common_set

# Тест
assert common_elements() == {0, 75, 45, 15, 90, 60, 30}
